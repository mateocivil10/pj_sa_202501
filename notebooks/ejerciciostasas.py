tasa_nominal_mensual = efectiva_anual_a_nominal_mensual(tasa_efectiva_anual_41_16)
print(f"Tasa nominal mensual: {tasa_nominal_mensual:.4f}")

# Cálculo de tasa efectiva anual desde semestral 12%
tasa_efectiva_semestral_12 = 0.12
tasa_efectiva_anual = efectiva_semestral_a_anual(tasa_efectiva_semestral_12)
